CREATE TABLE GAME_LIKE (
    LIKE_IDX       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID        NVARCHAR2(10) NOT NULL,
    GAME_IDX       VARCHAR2(10) NOT NULL,
    TITLE          NVARCHAR2(300),
    RELEASE_DATE   DATE,
    IMAGE_URL      VARCHAR2(500)
);

-- 주석 (COMMENT)
COMMENT ON TABLE GAME_LIKE IS '관심게임 페이지';
COMMENT ON COLUMN GAME_LIKE.LIKE_IDX      IS '고유번호';
COMMENT ON COLUMN GAME_LIKE.USER_ID       IS '내용 – USER FK';
COMMENT ON COLUMN GAME_LIKE.GAME_IDX      IS 'RAWG 게임 고유 ID 또는 slug';
COMMENT ON COLUMN GAME_LIKE.TITLE         IS '게임 타이틀';
COMMENT ON COLUMN GAME_LIKE.RELEASE_DATE  IS '게임 출시일 (YYYY-MM-DD)';
COMMENT ON COLUMN GAME_LIKE.IMAGE_URL     IS '썸네일 이미지 URL';
